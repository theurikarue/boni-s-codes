#In Python programming, a dictionary is a built-in data structure that stores a collection of key-value pairs. It is also known as an associative array, map, or hash table in other programming languages.

#A dictionary is created using curly braces {} and consists of comma-separated key-value pairs. The keys in a dictionary are unique and immutable, while the values can be of any data type such as integers, strings, lists, or even other dictionaries.

#Here's an example of a dictionary in Python:*/
student = {
    "name": "John",
    "age": 20,
    "major": "Computer Science",
    "gpa": 3.5
}
#In this example, the dictionary student contains four key-value pairs. The keys are "name", "age", "major", and "gpa", and their corresponding values are "John", 20, "Computer Science", and 3.5, respectively.

#You can access the values in a dictionary by referencing their corresponding keys. For example, student["name"] would return "John", student["age"] would return 20, and so on.

#Dictionaries are useful when you want to store and retrieve data using a unique identifier (the key) rather than relying on the position of the data within the structure. They provide efficient lookup and retrieval of values based on keys, making them suitable for scenarios where quick access to data is required.

#Additionally, dictionaries in Python are mutable, meaning you can modify the values associated with keys or add new key-value pairs as needed.

